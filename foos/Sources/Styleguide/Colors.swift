import UIKit

public extension UIColor {
    enum Custom {
        public static let backgroundMain = UIColor(named: "backgroundMain", in: .main, compatibleWith: nil)!
        public static let backgroundBar = UIColor(named: "backgroundBar", in: .main, compatibleWith: nil)!
        public static let accent = UIColor(named: "accent", in: .main, compatibleWith: nil)!
    }
}

